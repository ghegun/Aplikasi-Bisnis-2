    private void BtnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditActionPerformed
        Pesan_Error.setText("");
        DefaultTableModel model=(DefaultTableModel) tabel_daftar.getModel();
       if(tabel_daftar.getSelectedRow()==-1){
           if(tabel_daftar.getRowCount()==0){
               Pesan_Error.setText("Tabel Kosong");
           }else{
               Pesan_Error.setText("Anda Harus Pilih Daftar Tabel");
           }
       }else{
            model.setValueAt(TxtNoPendaftaran.getText(), tabel_daftar.getSelectedRow(), 0);
            model.setValueAt(TxtIdPegawai.getText(), tabel_daftar.getSelectedRow(), 1);
            model.setValueAt(TxtNoAntrian.getText(), tabel_daftar.getSelectedRow(), 2);
            model.setValueAt(TxtTgl.getText(), tabel_daftar.getSelectedRow(), 3);
            model.setValueAt(TxtKodePasien.getText(), tabel_daftar.getSelectedRow(), 4);
            model.setValueAt(TxtKeluhan.getText(), tabel_daftar.getSelectedRow(), 5);
            model.setValueAt(TxtKodeDokter.getText(), tabel_daftar.getSelectedRow(), 6);
            model.setValueAt(TxtBiaya.getText(), tabel_daftar.getSelectedRow(), 7);
            }
    }//GEN-LAST:event_BtnEditActionPerformed

    private void tabel_daftarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_daftarMouseClicked
        DefaultTableModel model=(DefaultTableModel) tabel_daftar.getModel();
        TxtNoPendaftaran.setText(model.getValueAt(tabel_daftar.getSelectedRow(), 0).toString());
        TxtIdPegawai.setText(model.getValueAt(tabel_daftar.getSelectedRow(), 1).toString());
        TxtNoAntrian.setText(model.getValueAt(tabel_daftar.getSelectedRow(), 2).toString());
        TxtTgl.setText(model.getValueAt(tabel_daftar.getSelectedRow(), 3).toString());
        TxtKodePasien.setText(model.getValueAt(tabel_daftar.getSelectedRow(), 4).toString());
        TxtKeluhan.setText(model.getValueAt(tabel_daftar.getSelectedRow(), 5).toString());
        TxtKodeDokter.setText(model.getValueAt(tabel_daftar.getSelectedRow(), 6).toString());
        TxtBiaya.setText(model.getValueAt(tabel_daftar.getSelectedRow(), 7).toString());
    }//GEN-LAST:event_tabel_daftarMouseClicked

