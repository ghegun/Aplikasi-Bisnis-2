    private void BtnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTambahActionPerformed
        Pesan_Error.setText("");
        DefaultTableModel model=(DefaultTableModel) tabel_daftar.getModel();
        if(!TxtNoPendaftaran.getText().trim().equals("")){
            model.addRow(new Object[]{TxtNoPendaftaran.getText(), 
            TxtIdPegawai.getText(),TxtTgl.getText(),TxtNoAntrian.getText(),
            TxtTgl.getText(), TxtKodePasien.getText(), 
            TxtKeluhan.getText(), TxtKodeDokter.getText(), 
            TxtBiaya.getText()});
        }else{
            Pesan_Error.setText("Kolom Harus Terisi Tidak Boleh Kosong");
        } 
        
        SwingUtil swingUtil = new SwingUtil();
        swingUtil.setActive(true, TxtNoPendaftaran, TxtIdPegawai, TxtTgl, 
                            TxtKodePasien, TxtNoAntrian, TxtKeluhan,
                            TxtKodeDokter, TxtBiaya );
        BtnSimpan.setEnabled(true);
    }//GEN-LAST:event_BtnTambahActionPerformed

    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
      String getTxtNoPendaftaran = TxtNoPendaftaran.getText();
      String getTxtIdPegawai = TxtIdPegawai.getText();
      String getTxtNoAntrian = TxtNoAntrian.getText();
      String getTxtTgl = TxtTgl.getText();
      String getTxtKodePasien = TxtKodePasien.getText();
      String getTxtKeluhan = TxtKeluhan.getText();
      String getTxtKodeDokter = TxtKodeDokter.getText();
      String getTxtBiaya =TxtBiaya.getText();
      
      Pendaftaran pendaftaran = new Pendaftaran(getTxtNoPendaftaran, getTxtIdPegawai, getTxtNoAntrian, getTxtTgl, getTxtKodePasien, getTxtKeluhan, getTxtKodeDokter, getTxtBiaya);
      
      listPendaftaran.add(Pendaftaran);
      
      
      
    }//GEN-LAST:event_BtnSimpanActionPerformed

